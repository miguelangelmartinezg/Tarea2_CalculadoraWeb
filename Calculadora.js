class Calculadora {
    sumar(nNumero1,nNumero2){
        return nNumero1 + nNumero2;
    }

    restar(nNumero1,nNumero2){
        return nNumero1 - nNumero2;
    }
    
    multiplicar(nNumero1,nNumero2){
        return nNumero1 * nNumero2;
    }

    dividir(nNumero1,nNumero2){
        return nNumero1 / nNumero2;
    }

    
}